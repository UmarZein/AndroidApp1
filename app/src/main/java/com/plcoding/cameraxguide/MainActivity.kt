    private fun takePhoto(
        controller: LifecycleCameraController,
        tts: TextToSpeech,
        onPhotoTaken: (Bitmap) -> Unit
    ) {
        controller.takePicture(
            ContextCompat.getMainExecutor(applicationContext),
            object : OnImageCapturedCallback() {
                override fun onCaptureSuccess(image: ImageProxy) {
                    super.onCaptureSuccess(image)

                    val matrix = Matrix().apply {
                        postRotate(image.imageInfo.rotationDegrees.toFloat())
                    }

                    val rotatedBitmap = Bitmap.createBitmap(
                        image.toBitmap(),
                        0,
                        0,
                        image.width,
                        image.height,
                        matrix,
                        true
                    )


                    val client = OkHttpClient()
                    val stream = ByteArrayOutputStream()
                    rotatedBitmap.compress(Bitmap.CompressFormat.JPEG, 75, stream)
                    val byteArray = stream.toByteArray()
                    Log.i(TAG,"byteArray size = ${byteArray.size}")
                    val requestBody: RequestBody = MultipartBody.Builder()
                        .setType(MultipartBody.FORM)
                        .addFormDataPart("image", "filename.jpg",
                            byteArray.toRequestBody(
                                "image/jpeg".toMediaTypeOrNull(),
                                0,
                                byteArray.size
                            )
                        )
                        .build()
                    val request: Request = Request.Builder()
                        .url("http://192.168.100.10:5000/process")
                        .post(requestBody)
                        .build()

                    client.newCall(request).enqueue(object : Callback {
                        override fun onFailure(call: Call, e: IOException) {
                            Log.e(TAG,"error: ${e.message}\n${e.stackTraceToString()}")
                        }

                        override fun onResponse(call: Call, response: Response) {
                            val body = response.body?.string();
                            tts.speak(body,TextToSpeech.QUEUE_FLUSH,null, null)
                            Log.d(TAG,"response message[${response.code}](${response.message}): $body")
                        }
                    })

                    onPhotoTaken(rotatedBitmap)
                }

                override fun onError(exception: ImageCaptureException) {
                    super.onError(exception)
                    Log.e("Camera", "Couldn't take photo: ", exception)
                }
            }
        )
    }

    private fun hasRequiredPermissions(): Boolean {
        return CAMERAX_PERMISSIONS.all {
            ContextCompat.checkSelfPermission(
                applicationContext,
                it
            ) == PackageManager.PERMISSION_GRANTED
        }
    }

